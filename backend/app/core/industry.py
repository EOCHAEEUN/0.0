# 업종코드 → 업종명 매핑 (단일 코드 기준)
INDUSTRY_CODE_TO_NAME = {
    "C": "전체 제조업",
    "C10": "식품 제조업",
    "C13": "섬유 제조업",
    "C20": "화학 제조업",
    "C21": "의약/바이오 제조업",
    "C22": "고무/플라스틱 제조업",
    "C24": "금속가공 제조업",
    "C25": "기타 금속 제조업",
    "C26": "전자/반도체 제조업",
    "C27": "의료기기 제조업",
    "C28": "전기장비 제조업",
    "C29": "기계/장비 제조업",
    "C30": "자동차/부품 제조업",
}


def name_to_codes(name: str) -> list[str]:
    """업종명 → 업종코드 리스트 반환"""
    return INDUSTRY_NAME_TO_CODE.get(name, [])


def code_to_name(code: str) -> str:
    """업종코드 → 업종명 반환"""
    return INDUSTRY_CODE_TO_NAME.get(code, "알 수 없음")


def get_all_industries() -> list[dict]:
    """전체 업종 목록 반환 (프론트 드롭다운용)"""
    return [
        {"name": name, "codes": codes}
        for name, codes in INDUSTRY_NAME_TO_CODE.items()
    ]
